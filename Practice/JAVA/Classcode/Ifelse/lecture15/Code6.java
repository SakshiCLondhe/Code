//Given an integer input print whether its odd or even 
//input- 7
//output-7 is odd
//if input is 4then gives output 4 is even
class Ifelse{
	public static void main(String args []){
		int x= 7;
		if(x%2==0){
			System.out.println(x+ "is even");
	}
		else {
			System.out.println(x+" is odd");
		}
	}
}

