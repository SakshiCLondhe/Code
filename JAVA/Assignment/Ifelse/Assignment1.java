//Write a java program to check if a no is even or odd
class Assignment1{
	public static void main(String args[]){
		int var=-9;
		if (var%2==0)
		{
			System.out.println(var+"is an even no");
		}
		else if( var%2!=0)
		{
			System.out.println(var+"is an odd no");
		
		}
		
		else {
			System.out.println(var+"neither even or odd");
	}
}}
